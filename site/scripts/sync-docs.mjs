import { mkdirSync, rmSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import {
  docsPages,
  generatedDocsDir,
  generatedRoot,
  parseProviderCategories,
  readUtf8,
  readmePath,
  removePromotedHeading,
  rewriteMarkdownLinks,
  shortDescription,
} from './docs-data.mjs';

function yamlString(value) {
  return JSON.stringify(value);
}

function frontmatter(fields) {
  const lines = ['---'];
  for (const [key, value] of Object.entries(fields)) {
    if (value === undefined) {
      continue;
    }
    if (typeof value === 'boolean' || typeof value === 'number') {
      lines.push(key + ': ' + value);
      continue;
    }
    if (typeof value === 'object') {
      lines.push(key + ':');
      for (const [childKey, childValue] of Object.entries(value)) {
        lines.push('  ' + childKey + ': ' + (typeof childValue === 'string' ? yamlString(childValue) : childValue));
      }
      continue;
    }
    lines.push(key + ': ' + yamlString(value));
  }
  lines.push('---', '');
  return lines.join('\n');
}

function generatedNotice() {
  return '<!-- Generated by npm run sync-docs. Edit README.md or docs/*.md instead. -->\n\n';
}

function writeGenerated(relativePath, fields, body) {
  const outputPath = join(generatedRoot, relativePath);
  mkdirSync(join(outputPath, '..'), { recursive: true });
  writeFileSync(outputPath, frontmatter(fields) + generatedNotice() + body.trim() + '\n');
}

function providersMarkdown(categories) {
  const sections = categories.map((category) => {
    const items = category.items
      .map((item) => '- [' + item.label + '](../' + item.slug + '/)')
      .join('\n');
    return '## ' + category.label + '\n\n' + items;
  });

  return [
    'Terraformer groups provider documentation by the import surface exposed in the CLI.',
    'Use these pages to find authentication notes, supported services, and provider-specific import examples.',
    '',
    ...sections,
  ].join('\n\n');
}

function docsIndexMarkdown() {
  return [
    'Use these docs to install Terraformer, choose a provider, and understand how provider import support is structured.',
    '',
    '## Start here',
    '',
    '- [Installation](./install/)',
    '- [Supported providers](./providers/)',
    '- [Provider architecture](./provider-architecture/)',
    '- [Unsupported resource metadata](./unsupported-resources/)',
  ].join('\n');
}

function homeMarkdown(providerCount) {
  return [
    'Terraformer imports existing infrastructure into Terraform configuration and state.',
    '',
    '## Quick links',
    '',
    '- [Install Terraformer](./docs/install/)',
    '- [Browse ' + providerCount + ' provider docs](./docs/providers/)',
    '- [Understand provider architecture](./docs/provider-architecture/)',
    '- [Review unsupported resource metadata](./docs/unsupported-resources/)',
    '',
    '## What Terraformer does',
    '',
    '- Generates Terraform configuration and state from existing resources.',
    '- Supports provider-specific filtering, resource selection, and state output options.',
    '- Keeps provider import logic focused on refreshable, secret-free configuration.',
  ].join('\n');
}

function extractInstallation(readme) {
  const start = readme.indexOf('### Installation');
  const end = readme.indexOf('## High-Level steps to add new provider');
  if (start === -1 || end === -1 || end <= start) {
    throw new Error('Could not extract installation section from README.md');
  }

  return readme
    .slice(start, end)
    .replace(/^### Installation\s*/, '')
    .trim();
}

function main() {
  const categories = parseProviderCategories();
  const pages = docsPages();

  rmSync(join(generatedRoot, 'index.md'), { force: true });
  rmSync(generatedDocsDir, { recursive: true, force: true });
  mkdirSync(generatedDocsDir, { recursive: true });

  const readme = readUtf8(readmePath);

  writeGenerated(
    'index.md',
    {
      title: 'Terraformer',
      description: 'Import existing infrastructure into Terraform configuration and state.',
      editUrl: false,
      sidebar: { hidden: true },
    },
    homeMarkdown(pages.filter((page) => page.isProvider).length)
  );

  writeGenerated(
    'docs/index.md',
    {
      title: 'Terraformer Docs',
      description: 'Install Terraformer, browse provider docs, and understand provider import behavior.',
      editUrl: false,
      sidebar: { label: 'Overview', order: 1 },
    },
    docsIndexMarkdown()
  );

  const installationBody = rewriteMarkdownLinks(extractInstallation(readme), 'docs/install', 'README.md');
  writeGenerated(
    'docs/install.md',
    {
      title: 'Installation',
      description: 'Install Terraformer and the Terraform provider plugins required for imports.',
      editUrl: false,
      sidebar: { order: 2 },
    },
    installationBody
  );

  writeGenerated(
    'docs/providers.md',
    {
      title: 'Supported Providers',
      description: 'Browse Terraformer provider documentation by provider category.',
      editUrl: false,
      sidebar: { order: 3 },
    },
    providersMarkdown(categories)
  );

  for (const page of pages) {
    const source = page.useHeadingAsTitle
      ? removePromotedHeading(page.markdown, page.title)
      : page.markdown;
    const body = rewriteMarkdownLinks(source, page.route, page.sourcePath);
    writeGenerated(
      'docs/' + page.slug + '.md',
      {
        title: page.title,
        description: page.isProvider ? page.description : shortDescription(source, page.description),
        editUrl: false,
      },
      body
    );
  }
}

main();
